//17.一个球从100米高度落下，每次落地后反跳回原来高度的一半；再落下，求它在第十次落地时，共经过多少米？第十次反弹多高？

#include <stdio.h>
int main()
{	
	//h为反弹高度，s为路程
	float h = 100;
	float s = 100;
	//首次削减反弹高度
	h = h / 2;
	int i;
	//每次路程加上两个反弹高度，反弹高度每次削减一半
	for (i = 2; i <= 10; i++) {
		s = s + h * 2;
		h = h / 2;
	}

	printf("路程:%f,高度:%f", s, h);
	return 0;
}