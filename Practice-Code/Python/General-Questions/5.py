"""
5.
猴子吃桃子问题：猴子第一天摘若干桃子，当天吃一半多 1 个；第二天早上吃剩下的一半多 1 个；
以此类推，第十天早上只剩 1 个桃子。需编写函数求第一天共摘的桃子数量。
"""

def monkey_peach():
    # 从第10天早上的1个桃子倒推第一天的数量
    peaches = 1  # 第10天早上剩余数量
    for _ in range(9):  # 倒推9天（第9天到第1天）
        peaches = (peaches + 1) * 2  # 逆运算：前一天数量 = (当天数量+1)×2
    return peaches

print(f"猴子第一天共摘了{monkey_peach()}个桃子")  # 结果：1534